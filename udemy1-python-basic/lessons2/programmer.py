class Programmer:

    def setName(self,name):
        self.name=name


    def getName(self):
        return self.name


    def setSalary(self,salary):
        self.salary=salary


    def getSalary(self):
        return self.salary


    def setTechnologies(self,techs):
        self.technologies=techs


    def getTechnologies(self):
        return self.technologies

